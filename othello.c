/*		othe_yomitst															*/
/********************************************************************************/
int othe_yomitst_s(char *ban, int k, int pos, int dir)
{
	int kk;
	
	if(ban[pos])							// 石無しでなければ
		return 0;							// 打てない
	pos += dir;
	if(ban[pos] != macroinv(k))				// 敵駒に接触していなければ
		return 0;							// 打てない
	for(;;){
		pos += dir;
		kk = ban[pos];
		if(kk==k)							// はさんでいる
			return 1;						// 打てる
		if(kk==ISHI_N || kk== ISHI_O)
			return 0;						// 打てない
	}
}

int othe_yomitst(char *ban, int k, int pos)
{
	int i;
	
	if(ban[pos])							// 石無しでなければ
		return 0;							// 打てない
	for(i=0; i<8; i++){
		if(othe_yomitst_s(ban, k, pos, othe_dir3[i]))	// dir3 方向で打てるか？
			return 1;						// 打てる
	}
	return 0;								// 打てない
}

/********************************************************************************/
/*		othe_get_upos															*/
/********************************************************************************/
void othe_get_upos(char *ban, int k, char upos[60], int *upos_cnt)
{
	int p;
	
	*upos_cnt = 0;
	for(p=10; p<81; p++){
		if(othe_yomitst(ban, k, p))				// 打てるか？
			upos[(*upos_cnt)++] = p;
	}
}

//		この盤面に打つ手があるか？
int upos_tst(char *ban, int k)
{
	char upos[60];
	int upos_cnt;
	
	othe_get_upos(ban, k, upos, &upos_cnt);
	if(upos_cnt==0)
		return 0;								// 打てない
	return 1;									// 打てる
}
/********************************************************************************/
/*		othe_yomiutu															*/
/*		開放度を返す															*/
/********************************************************************************/
int othe_yomiutu(char *ban, int k, int pos)
{
	int i, j, s, p, dir, kaiho;
	
	s = macroinv(k);
	kaiho = 0;
	for(i=0; i<8; i++){
		dir = othe_dir3[i];
		if(othe_yomitst_s(ban, k, pos, dir)){	// dir 方向で打てるか？
			p = pos;
			for(;;){
				p += dir;
				if(ban[p]==s){
					ban[p] = k;
					for(j=0; j<8; j++){
						if(ban[p+othe_dir3[j]]==ISHI_N)
							++kaiho;
					}
				}
				else{
					break;
				}
			}
		}
	}
	ban[pos] = k;
	return kaiho;
}
/********************************************************************************/
/*		othe_result																*/
/*		石数を数える、黒+														*/
/********************************************************************************/
int othe_result(char *ban)
{
	int ten, p;
	
	ten = 0;
	for(p=10; p<81; p++){
		if(ban[p]==ISHI_K)
			++ten;
		else if(ban[p]==ISHI_S)
			--ten;
	}
	return ten;
}
