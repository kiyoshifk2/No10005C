/*		othe_game																*/
/*		ゲーム進行のメインルーチン												*/
/********************************************************************************/
void othe_game()
{
	int player;								// 0:cpu, 1:human
	int pos, ten;
	long eva;
	char str[50];
	
	player = othe_v.teban==ISHI_K ? othe_v.black : othe_v.white;
	lcd_disp_str_x1(20+(9-1)*24+12, 26, player ? "human    " : "cpu     ");
	pos = macropos(1,1);
	for(;;){
		/***	パスの処理	***/
		if(upos_tst(othe_v.goban, othe_v.teban)==0){		// 打てない
			othe_v.teban = macroinv(othe_v.teban);
			if(upos_tst(othe_v.goban, othe_v.teban)==0){	// 相手番でも打てない
				ten = othe_result(othe_v.goban);
				if(ten==0)
					lcd_disp_str_x1(20+(9-1)*24+12, 66, "Draw     ");
				else{
					lcd_disp_str_x1(20+(9-1)*24+12, 66, ten>0 ? "Black    " : "White    ");
					sprintf(str, "%d", abs1(ten));
					lcd_disp_str_x1(20+(9-1)*24+12+12*6, 66, str);
				}
    			lcd_disp_str_x1(20+(9-1)*24+12, 106, "             ");
				othe_dispban(othe_v.goban, pos);
				sw_updownup();
				return;
			}
			lcd_disp_str_x1(20+(9-1)*24+12, 106, "  ｳﾃﾅｲ  ");
            othe_dispban(othe_v.goban, pos);
			sw_updownup();
			lcd_disp_str_x1(20+(9-1)*24+12, 106, "        ");
		}
		player = othe_v.teban==ISHI_K ? othe_v.black : othe_v.white;
		lcd_disp_str_x1(20+(9-1)*24+12, 26, player ? "human    " : "cpu     ");
		if(player==0){						// cpu の手番
			lcd_disp_str_x1(20+(9-1)*24+12, 106, "thinking  ");
			othe_dispban(othe_v.goban, pos);
			pos = othe_gametop(&eva);
			buzzer_flag = 1;				// 短音を鳴らす
			if(othe_yomitst(othe_v.goban, othe_v.teban, pos)==0)
				message("game error [%d,%d]", macrox(pos), macroy(pos));
		}
		else{								// 人間の手番
			lcd_disp_str_x1(20+(9-1)*24+12, 106, "ｳｯﾃｸﾀﾞｻｲ  ");
			for(;;){
				othe_dispban(othe_v.goban, pos);
				pos = othe_human_input(pos);
				if (othe_yomitst(othe_v.goban, othe_v.teban, pos))
					break;
			lcd_disp_str_x1(20+(9-1)*24+12, 106, "ｳﾃﾏｾﾝ    ");
			}
		}
        if(othe_v.brink_flag){
            othe_brinking(othe_v.teban, pos);
        }
		othe_yomiutu(othe_v.goban, othe_v.teban, pos);
		othe_v.teban = macroinv(othe_v.teban);
		++othe_v.tekazu;
	}
}
