	cmd = menu_select(play_menu);
	if(cmd==1){
		othe_v.black = 0;
		othe_v.white = 0;
	}
	else if(cmd==2){
		othe_v.black = 1;
		othe_v.white = 1;
	}
	else if(cmd==3){
		othe_v.black = 0;
		othe_v.white = 1;
	}
	else{
		othe_v.black = 1;
		othe_v.white = 0;
	}
	
