void othe_dispban(char ban[100], int pos)
{
	int x, y, p, k;
	
	lcd_clearA(20-1,    28-1,    20-1,    28+8*24, BLACK);
	lcd_clearA(20+8*24, 28-1,    20+8*24, 28+8*24, BLACK);
	lcd_clearA(20-1,    28-1,    20+8*24, 28-1,    BLACK);
	lcd_clearA(20-1,    28+8*24, 20+8*24, 28+8*24, BLACK);
	for(x=1; x<=8; x++){
		for(y=1; y<=8; y++){
			p = macropos(x,y);
			k = ban[p];
			if(k==ISHI_N)
				othe_koma0(x,y);
			else if(k==ISHI_K)
				othe_koma1(x,y);
			else if(k==ISHI_S)
				othe_koma2(x,y);
		}
	}
	for(x=1; x<=8; x++){
		lcd_disp_char_x1(20+(x-1)*24+6, 6, 'A'+x-1);
	}
	for(y=1; y<=8; y++){
		lcd_disp_char_x1(4, 28+(y-1)*24+4, '1'+y-1);
	}
    x = macrox(pos);
    y = macroy(pos);
    if(ban[pos]==ISHI_K)
    	othe_koma1_c(x,y);						// 黒カーソル
    else if(ban[pos]==ISHI_S)
    	othe_koma2_c(x,y);						// 白カーソル
    else
    	othe_koma0_c(x, y);						// 盤カーソル
}
