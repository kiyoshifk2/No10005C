int othe_human_input(int pos)
{
	int x, y, p, key;
	
	x = macrox(pos);
	y = macroy(pos);
	for(;;){
		p = macropos(x,y);
		othe_dispban(othe_v.goban, p);
		key = sw_updownup();
		if(key & 1){
			if(++x >= 9)
				x = 1;
		}
		else if(key & 2){
			if(++y >= 9)
				y = 1;
		}
		else if(key & 4){
			return macropos(x,y);
		}
	}
}
