void lcd_scroll()
{
	int i, k;
	short buf[320];
	
	memmove(lcd_text_buf, lcd_text_buf[1], 26*(lcd_noofline-1));
	memset(lcd_text_buf[lcd_noofline-1], ' ', 26);

	for(i=16; i<16*lcd_noofline; i++){
		lcd_read_mem(0, i, 319, i, buf);

		LCD_ALL_S;							// idle
		LCD_CS_C;							// CS clear
		lcd_area_set(0, i-16, 319, i-16);
		lcd_write_cmd(MemoryWrite);
		for(k=0; k<320; k++)
			lcd_write_color(buf[k]);
		LCD_ALL_S;							// idle
	}
	lcd_clearA(0, 16*(lcd_noofline-1), 319, 16*lcd_noofline-1, back_color);	// 最下行クリア
}

void lcd_nl()
{
	lcd_x = 0;
	if(++lcd_y >= lcd_noofline){
		lcd_scroll();						// スクロール
		lcd_y = lcd_noofline - 1;
	}
}

void lcd_putc(char c)
{
	if(c=='\n'){
		lcd_nl();							// new line
		return;
	}
	if(lcd_x > 25){
		lcd_nl();
	}
	lcd_disp_char_x1(lcd_x*12, lcd_y*16, c);
	lcd_text_buf[lcd_y][lcd_x] = c;
	++lcd_x;
//	if(++lcd_x > 25){
//		lcd_nl();							// new line
//	}
}
/********************************************************************************/
/*		lcd_disp_str_x1															*/
/*		キャラゼネは 10x14 だが縁をつけて 12x16 ドットの文字を描く				*/
/*		text_color, back_color の色指定に従う									*/
/********************************************************************************/
void lcd_disp_str_x1(int x, int y, const char *str)
{
	while(*str){
		lcd_disp_char_x1(x, y, *str);
		++str;
		x += 12;
	}
}

void lcd_disp_str_x2(int x, int y, const char *str)
{
	while(*str){
		lcd_disp_char_x2(x, y, *str);
		++str;
		x += 24;
	}
}

void lcd_cdisp_str_x1(int y, const char *str)
{
    int x;
    
    x = (320-strlen(str)*12)/2;
    if(x<0)
        x = 0;
    lcd_disp_str_x1(x, y, str);
}

void lcd_cdisp_str_x2(int y, const char *str)
{
    int x;
    
    x = (320-strlen(str)*24)/2;
    if(x<0)
        x = 0;
    lcd_disp_str_x2(x, y, str);
}
/********************************************************************************/
/*		lcd_disp_char_x1														*/
/*		12x16 ドットの文字を描く												*/
/*		text_color, back_color の色指定に従う									*/
/*		return 0:success, -1:error												*/
/********************************************************************************/
int lcd_disp_char_x1(int x, int y, char c)
{
	int i, k, x2, y2, cg /*, color*/;
	
	x2 = x + (12-1);
	y2 = y + (16-1);
	LCD_ALL_S;							// idle
	LCD_CS_C;							// CS clear
	if(lcd_area_set(x, y, x2, y2)<0){
		LCD_ALL_S;
		return -1;						// error
	}
	lcd_write_cmd(MemoryWrite);
	for(i=0; i<16; i++){				// 16 行を順番に処理する
		cg = font[(unsigned char)c][i];	// キャラゼネ１行
		for(k=0; k<12; k++){
			if(cg & 1)
				lcd_write_color(text_color);
			else
				lcd_write_color(back_color);
			cg >>= 1;
		}
	}
	LCD_ALL_S;							// idle
	return 0;							// success
}

int lcd_disp_char_x2(int x, int y, char c)
{
	int i, k, m, x2, y2, cg /*, color*/;
	
	x2 = x + (24-1);
	y2 = y + (32-1);
	LCD_ALL_S;							// idle
	LCD_CS_C;							// CS clear
	if(lcd_area_set(x, y, x2, y2)<0){
		LCD_ALL_S;
		return -1;						// error
	}
	lcd_write_cmd(MemoryWrite);
	for(i=0; i<16; i++){				// 16 行を順番に処理する
		for(m=0; m<2; m++){
			cg = font[(unsigned char)c][i];	// キャラゼネ１行
			for(k=0; k<12; k++){
				if(cg & 1){
					lcd_write_color(text_color);
					lcd_write_color(text_color);
				}
				else{
					lcd_write_color(back_color);
					lcd_write_color(back_color);
				}
				cg >>= 1;
			}
		}
	}
	LCD_ALL_S;							// idle
	return 0;							// success
}
/********************************************************************************/
/*		lcd_area_set															*/
/*		四角の座標を lcd にセットする											*/
/********************************************************************************/
int lcd_area_set(int x1, int y1, int x2, int y2)
{
	if(x1<0 || x1>=320 || x2<0 || x2>=320 || y1<0 || y1>=240 || y2<0 || y2>=240 || x1>x2 || y1>y2)
		return -1;						// error
	lcd_write_cmd(ColumnAddressSet);
	lcd_write_data(x1>>8);				// start 上位
	lcd_write_data(x1);					// start 下位
	lcd_write_data(x2>>8);				// end 上位
	lcd_write_data(x2);					// end 下位
	
	lcd_write_cmd(PageAddressSet);
	lcd_write_data(y1>>8);				// start 上位
	lcd_write_data(y1);					// start 下位
	lcd_write_data(y2>>8);				// end 上位
	lcd_write_data(y2);					// end 下位
	return 0;							// success
}
/********************************************************************************/
/*		lcd_clear																*/
/*		指定範囲を塗りつぶす													*/
/********************************************************************************/
int lcd_clearA(int x1, int y1, int x2, int y2, int color)
{
	int i;
	
	LCD_ALL_S;							// idle
	LCD_CS_C;							// CS clear
	lcd_area_set(x1, y1, x2, y2);
	lcd_write_cmd(MemoryWrite);
	for(i=0; i<(x2-x1+1)*(y2-y1+1); i++)
		lcd_write_color(color);
	LCD_ALL_S;							// idle
	return 0;							// success
}

