/********************************************************************************/
/*		lcd_write_data															*/
/********************************************************************************/
void lcd_write_data(int data)
{
	LCD_OUTPUT(data);
	
	LCD_WR_C;								// WR clear
	LCD_WR_C;								// WR clear
	LCD_WR_S;								// WR set
}
/********************************************************************************/
/*		lcd_read_data															*/
/********************************************************************************/
int lcd_read_data()
{
	int tmp;
	
	LCD_RD_C;							// RD clear
	LCD_RD_C;							// RD clear
	tmp = LCD_INPUT;					// read data
	LCD_RD_S;							// RD set
	
	return tmp;
}
/********************************************************************************/
/*		lcd_write_cmd															*/
/********************************************************************************/
void lcd_write_cmd(int cmd)
{
	LCD_RS_C;							// RS clear
	LCD_OUTPUT(cmd);					// output cmd
	LCD_WR_C;							// WR clear
	LCD_WR_C;							// WR clear
	LCD_WR_S;							// WR set
	LCD_WR_S;							// WR set
	LCD_RS_S;							// RS set
}
