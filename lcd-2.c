/********************************************************************************/
/*		wait_1ms																*/
/********************************************************************************/
void wait_1ms()
{
	int t = TickCount;
	
	while(TickCount-t < 10)
		;
}
void wait_1ms_lcd()
{
	volatile int i;
	
	for(i=0; i<7500; i++) ;    // 9000:96MHz, 7500:80MHz
}

void wait_ms(int ms)
{
	while(ms>0){
		wait_1ms();
		--ms;
	}
}
/********************************************************************************/
/*		lcd_read_mem															*/
/********************************************************************************/
void lcd_read_mem(int x1, int y1, int x2, int y2, short *buf)
{
	int i, c1, c2, c3;
	
	LCD_ALL_S;								// all set, idle
	LCD_CS_C;								// cs clear
	lcd_area_set(x1, y1, x2, y2);
	lcd_write_cmd(MemoryRead);

	LCD_DIR_IN;								// data port input
    lcd_read_data();
    lcd_read_data();
	for(i=0; i<(x2-x1+1)*(y2-y1+1); i++){
		c1 = lcd_read_data();
		c2 = lcd_read_data();
		c3 = lcd_read_data();
		*buf++ = RGB(c3, c2, c1);
	}
	LCD_DIR_OUT;							// data port output
	LCD_ALL_S;								// idle
}
