/********************************************************************************/
/*		pen_ban_init															*/
/********************************************************************************/
void pen_ban_init()
{
	int x, y;
	
	memset(pen_ban, 0, sizeof(pen_ban));
	for(y=0; y<8; y++){
		pen_ban[y][0] = 1;
		pen_ban[y][12-1] = 1;
	}
	for(x=0; x<12; x++){
		pen_ban[0][x] = 1;
		pen_ban[8-1][x] = 1;
	}
}
/********************************************************************************/
/*		pen_put_to_ban															*/
/********************************************************************************/
void pen_put_to_ban(struct pen_move *move)
{
	int cnt, p;
	char *ban = (char*)pen_ban;
	
	if(peni[move->i][move->k][0]==10000)
		message("put_to_ban err1 i=%d k=%d\n", move->i, move->k);
	for(cnt=0; cnt<5; cnt++){
		p = peni[move->i][move->k][cnt];
		ban[move->pos + p] = 1;
	}
}
/********************************************************************************/
/*		pen_remove_from_ban														*/
/********************************************************************************/
void pen_remove_from_ban(struct pen_move *move)
{
	int cnt, p;
	char *ban = (char*)pen_ban;
	
	if(peni[move->i][move->k][0]==10000)
		message("remove_from_ban err1 i=%d k=%d\n", move->i, move->k);
	for(cnt=0; cnt<5; cnt++){
		p = peni[move->i][move->k][cnt];
		ban[move->pos + p] = 0;
	}
}
/********************************************************************************/
/*		pen_top_pos																*/
/*		盤の空き位置のうち、左の上を返す										*/
/********************************************************************************/
int pen_top_pos()
{
	int x, y, p;
	char *ban = (char*)pen_ban;
    
	for(x=1; x<=10; x++){
		for(y=1; y<=6; y++){
			p = macropos(x,y);
			if(ban[p]==0)
				return p;
		}
	}
	message("pen_top_pos err1\n");
    return 0;
}
/********************************************************************************/
/*      pen_test                                                                */
/********************************************************************************/
void pen_test()
{
    int x, y;
    char *ban = (char*)pen_ban;
    
    for(x=0; x<12; x++){
        if(ban[macropos(x,0)]==0)
            goto err1;
        if(ban[macropos(x,8-1)]==0)
            goto err1;
    }
    for(y=0; y<8; y++){
        if(ban[macropos(0,y)]==0)
            goto err1;
        if(ban[macropos(12-1,y)]==0)
            goto err1;
    }
    return;
    
    err1:; message("pen_test error\n");
}
/********************************************************************************/
/*		pen_disp_all															*/
/********************************************************************************/
void pen_disp_all()
{
	int i;
	
	for(i=0; i<12; i++){
		pen_put_to_disp(&pen_move[i]);
	}
}
/********************************************************************************/
/*		pentomino																*/
/********************************************************************************/
void pentomino(int fast)
{
	int sw, ii, m, up, use[12], count;
	
    make_peni();
    pen_ban_init();
    lcd_clear(WHITE);
    pen_waku();
	memset(use, 0, sizeof(use));
    count = 0;
	ii = 0;
	up = 1;
	for(;;){
		if(fast==0){
			sw = sw_read();
			if(sw){
				sw_downup();
				return;
			}
		}
//		pen_test();//AAAAA 盤の縁をテスト
		if(up==1){							// 前進した
			for(m=0; m<12; m++){
				if(use[m]==0){
					use[m] = 1;
					break;
				}
			}
			if(m==12)
				message("pentomino err1 ii=%d\n", ii);
			pen_move[ii].i = m;
			pen_move[ii].k = 0;
			pen_move[ii].pos = pen_top_pos();
		}
		else{								// 続きを実行 (後退した)
            if(up==0){
    			pen_remove_from_ban(&pen_move[ii]);
    			if(fast==0)
					pen_remove_from_disp(&pen_move[ii]);
            }
			++pen_move[ii].k;
			if(pen_move[ii].k >= 8 || peni[pen_move[ii].i][pen_move[ii].k][0] == 10000){	// 回転方向全部試した
				for(m=pen_move[ii].i+1; m<12; m++){
					if(use[m]==0){
						use[m] = 1;
						break;
					}
				}
				use[pen_move[ii].i] = 0;
				if(m==12){						// 全部テストしたから後退する
					up = 0;
					ii--;
					if(ii<0){					// 終了
						lcd_printf("pentomino end\n");
                        sw_updownup();
						return;
					}
					continue;
				}
				pen_move[ii].i = m;
				pen_move[ii].k = 0;
			}
		}
		if(pen_test_move(&pen_move[ii])){	// 置けるならば
			pen_put_to_ban(&pen_move[ii]);	// 盤に置く
			if(fast==0){
				pen_put_to_disp(&pen_move[ii]);	// 表示する
				wait_ms(100);
			}
			if(ii== 12-1){					// 完成した
				if(fast==0){
					lcd_clear(RED);
					pen_waku();
					pen_disp_all();
					wait_ms(10000);
					
					lcd_clear(WHITE);
					pen_waku();
					pen_disp_all();
				}
				else{
                    lcd_x = lcd_y = 0;
                    lcd_printf("count=%d\n", ++count);
					pen_disp_all();
					sw = sw_read();
					if(sw){
						sw_downup();
						return;
					}
				}
				up = 0;
			}
			else{							// 未完成, 前進する
				up = 1;
				ii++;
			}
		}
		else{								// 置けない
			up = 2;							// 続きを実行
		}
	}
}






